!-----------------------------------------------------------------------------------------------!
SUBROUTINE SPLIN(NX,NY,IL)
!-----------------------------------------------------------------------------------------------!
IMPLICIT DOUBLE PRECISION (A-H,O-Z)
!-----------------------------------------------------------------------------------------------!
PARAMETER(NDIM=150)
!-----------------------------------------------------------------------------------------------!
COMMON /COOR/  X(0:NDIM+1,0:NDIM+1),Y(0:NDIM+1,0:NDIM+1)
COMMON /BOUN/  XL(NDIM,4),YL(NDIM,4),DXDL(NDIM,4),DYDL(NDIM,4),RL(NDIM,4)
!-----------------------------------------------------------------------------------------------!
IF (IL == 1) THEN
   DO I=1,NX
      XL(I,1)=X(1,I)
      YL(I,1)=Y(1,I)
   END DO !I=1,NX
   NLOC=NX
END IF !(IL == 1)
!-----------------------------------------------------------------------------------------------!
IF (IL == 2) THEN
   DO J=1,NY
      XL(J,2)=X(J,1)
      YL(J,2)=Y(J,1)
   END DO !J=1,NY
   NLOC=NY
END IF !(IL == 2)
!-----------------------------------------------------------------------------------------------!
IF (IL == 3) THEN
   DO I=1,NX
      XL(I,3)=X(NY,I)
      YL(I,3)=Y(NY,I)
   END DO !I=1,NX
   NLOC=NX
END IF !(IL == 3)
!-----------------------------------------------------------------------------------------------!
IF (IL == 4) THEN
   DO J=1,NY
      XL(J,4)=X(J,NX)
      YL(J,4)=Y(J,NX)
   END DO !J=1,NY
   NLOC=NY
END IF !(IL == 4)
!-----------------------------------------------------------------------------------------------!
CALL COEF(XL(1,IL),YL(1,IL),DXDL(1,IL),DYDL(1,IL),RL(1,IL),NLOC)
!-----------------------------------------------------------------------------------------------!
END SUBROUTINE SPLIN
